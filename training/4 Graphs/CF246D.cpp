#include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef long double ld;
typedef complex<ld> cd;

typedef pair<int, int> ii;
typedef tuple<int, int, int> iii;

typedef vector<int> vi;
typedef vector<ii> vii;
typedef vector<ld> vd;
typedef vector<ll> vl;

typedef set<int> si;
typedef set<ii> sii;
typedef set<ld> sd;
typedef set<ll> sl;

typedef map<int, int> mii;
typedef priority_queue<int> pqi;
typedef queue<int> qi;
 
#define mp make_pair
#define pb push_back
#define f first
#define s second

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n, m;
    cin >> n >> m;
    vi colour(n); for (auto &x : colour) cin >> x;
    map<int, si> neighbours;

    for (int i = 0, u, v; i < m; i++) {
        cin >> u >> v; u--, v--;
        if (colour[u] != colour[v]) {
            neighbours[colour[u]].insert(colour[v]);
            neighbours[colour[v]].insert(colour[u]);
        }
    }

    int ans = colour[0];
    for (auto &x : colour) {
        if (neighbours[x].size() > neighbours[ans].size()) ans = x;
        else if (neighbours[x].size() == neighbours[ans].size()) ans = min(ans, x);
    }
    cout << ans << endl;
}

