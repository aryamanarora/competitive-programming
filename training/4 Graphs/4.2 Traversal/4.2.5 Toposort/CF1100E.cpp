#define mp make_pair
#define pb push_back
#define f first
#define s second

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n, m;
    cin >> n >> m;
    vector<vii> g(n);
    for (int i = 0, u, v, w; i < m; i++) {
        cin >> u >> v >> w; u--, v--;
        g[u].pb({v, w});
    }

    auto has_cycles = [&](int u, int k, vector<bool> &vis) -> bool {
        vis[u] = true;
        for (auto &v : g[u]) {
            if (v.s < k) continue;
            if (vis[u]) return false;
            return dfs(v.f, k, vis);
        }
    };
}

