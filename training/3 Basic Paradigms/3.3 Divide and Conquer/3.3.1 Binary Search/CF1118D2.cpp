#define mp make_pair
#define pb push_back
#define f first
#define s second

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    ll n, m;
    cin >> n >> m;
    vi a(n); for (auto &x : a) cin >> x;
    sort(a.rbegin(), a.rend());

    auto check = [&](int days) {
        ll res = 0;
        for (int i = 0; i < n; i++) {
            res += max(0, a[i] - (i / days));
        }
        return res >= m;
    };

    int lo = 1, hi = n;
    while (lo <= hi) {
        int mid = (lo + hi) / 2;
        if (check(mid)) hi = mid - 1;
        else lo = mid + 1;
    }

    cout << (lo > n ? -1 : lo) << endl;
}

