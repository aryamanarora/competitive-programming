int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int lo = 1, hi = 1000, mid;
    string res;
    while (lo <= hi) {
        mid = (lo + hi) / 2;
        cout << mid << endl;
        cin >> res;
        if (res == "higher") lo = mid + 1;
        else if (res == "lower") hi = mid - 1;
        else return 0;
    }
    cout << mid << endl;
    cin >> res;
}

