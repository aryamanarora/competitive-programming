int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    
    int n, x;
    cin >> n >> x;
    multiset<int> a;
    for (int i = 0, j; i < n; i++) {
        cin >> j;
        a.insert(j);
    }

    int ans = 0;
    while (!a.empty()) {
        int cur = *a.rbegin();
        a.erase(--a.end());
        ans++;

        auto it = a.upper_bound(x - cur);
        if (a.empty()) break;
        else if (x == cur) continue;
        else if (it == a.begin()) continue;
        else {
            it--;
            a.erase(it);
        }
    }
    cout << ans << endl;
}

