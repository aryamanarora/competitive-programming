typedef set<ld> sd;
typedef set<ll> sl;

typedef map<int, int> mii;
typedef priority_queue<int> pqi;
typedef queue<int> qi;
 
#define mp make_pair
#define pb push_back
#define f first
#define s second

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n, m;
    while (cin >> n >> m) {
        if (n == 0 and m == 0) break;

        vi a(n), b(m);
        for (auto &x : a) cin >> x;
        for (auto &x : b) cin >> x;

        int ai = 0, bi = 0;
        int ans = 0;
        while (ai < n and bi < m) {
            if (a[ai] == b[bi]) ans++, ai++, bi++;
            else if (a[ai] > b[bi]) bi++;
            else ai++;
        }

        cout << ans << '\n';
    }
}

