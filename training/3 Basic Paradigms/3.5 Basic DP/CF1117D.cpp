int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n, m;
    cin >> n >> m;
    vi dp(n + 1, 1);
    dp[0] = 0;
    for (int i = 1; i < m; i++) cout << 1 << " ";
    for (int i = m; i <= n; i++) {
        dp[i] += dp[i - m] + dp[i - 1];
        cout << dp[i] << " ";
    }
    cout << endl;
}

