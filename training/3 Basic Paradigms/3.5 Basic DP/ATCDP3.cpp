int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n;
    cin >> n;
    vector<vi> a(n, vi(3));
    for (auto &x : a) for (auto &y : x) cin >> y;

    vector<vi> dp(n, vi(3));
    dp[0] = a[0];
    for (int i = 1; i < n; i++)
        for (int j = 0; j < 3; j++)
            for (int k = 0; k < 3; k++)
                if (j != k) dp[i][j] = max(dp[i][j], a[i][j] + dp[i - 1][k]);
    cout << max(dp[n - 1][0], max(dp[n - 1][1], dp[n - 1][2])) << endl;
}

