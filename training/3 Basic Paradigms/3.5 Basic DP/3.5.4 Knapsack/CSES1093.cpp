const int MOD = 1e9 + 7;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n;
    cin >> n;
    ll sum = (n * (n + 1)) / 2;
    if (sum % 2) {
        cout << 0 << endl;
        return 0;
    }

    sum /= 2;
    vl dp1(sum + 1), dp2(sum + 1);
    dp1[0] = 1;
    for (int i = 1; i <= n; i++) {
        for (int j = i; j <= sum; j++) dp2[j] = (dp1[j - i] + dp2[j]) % MOD;
        dp1 = dp2;
    }
    
    cout << dp1[sum] << endl;
}

