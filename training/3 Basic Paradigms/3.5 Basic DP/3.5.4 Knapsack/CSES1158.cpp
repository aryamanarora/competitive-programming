int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n, W;
    cin >> n >> W;
    vi we(n), v(n);
    for (auto &x : we) cin >> x;
    for (auto &x : v) cin >> x;

    vector<vi> dp(n + 1, vi(W + 1));
    for (int i = 1; i <= n; i++) {
        for (int w = 0; w <= W; w++) {
            dp[i][w] = max(
                dp[i - 1][w],
                (w >= we[i - 1] ? v[i - 1] + dp[i - 1][w - we[i - 1]] : 0)
            );
        }
    }

    cout << dp[n][W] << endl;
}

