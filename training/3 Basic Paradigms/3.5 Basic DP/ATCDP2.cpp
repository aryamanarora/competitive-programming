int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
  
  	int n, k;
  	cin >> n >> k;
  	vi a(n); for (auto &x : a) cin >> x;
  	vi dp(n, numeric_limits<int>::max()); dp[0] = 0;
  	for (int i = 0; i < n; i++) {
		for (int j = 1; j <= k and i + j < n; j++) dp[i + j] = min(dp[i + j], dp[i] + abs(a[i] - a[i + j]));
    }
    cout << dp[n - 1] << endl;
}

