#define f first
#define s second

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    string s;
    cin >> s;
    int n = s.length();
    stack<pair<char, int>> st;
    st.push({s[0], 0});
    for (int i = 1; i < n; i++) {
        if (st.empty()) {
            st.push({s[i], i});
        }
        else if (st.top().f == s[i]) {
            s[st.top().s] = ' ';
            s[i] = ' ';
            st.pop();
        }
        else {
            st.push({s[i], i});
        }
    }

    for (auto x : s) if (x != ' ') cout << x;
    cout << endl;
}

