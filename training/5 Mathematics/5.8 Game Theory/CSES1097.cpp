mt19937 rng((int) std::chrono::steady_clock::now().time_since_epoch().count());

ll a[5000][5000] = {0};
ll b[5000][5000] = {0};

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n;
    cin >> n;
    for (int i = 0; i < n; i++) cin >> a[i][i];

    for (int l = 1; l < n; l++) {
        for (int i = 0; i < n - l; i++) {
            int j = i + l;
            ll sum = a[i][i] + a[i + 1][j] + b[i + 1][j];
            a[i][j] = max(a[i][i] + b[i + 1][j], a[j][j] + b[i][j - 1]);
            b[i][j] = sum - a[i][j];
        }
    }

    cout << a[0][n - 1] << endl;
}

