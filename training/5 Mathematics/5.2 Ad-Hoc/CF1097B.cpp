#define mp make_pair
#define pb push_back
#define f first
#define s second

int n, ans = 0;

void dfs(int i, int tot, vi &a) {
    if (i == n) {
        if (tot == 0) ans++;
        return;
    }
    dfs(i + 1, (tot + a[i]) % 360, a);
    dfs(i + 1, (tot - a[i]) % 360, a);
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    cin >> n;
    vi a(n); for (auto &x : a) cin >> x;
    dfs(0, 0, a);
    cout << (ans == 0 ? "NO" : "YES") << endl; 
}

