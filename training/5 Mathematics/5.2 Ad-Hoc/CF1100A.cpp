#define pb push_back
#define f first
#define s second

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n, k;
    cin >> n >> k;
    vi a(n); int bad = 0;
    for (auto &x : a) {
        cin >> x;
        if (x == -1) bad++;
    }

    int ans = 0;

    for (int i = 0; i < k; i++) {
        int g = (n - bad), b = bad;
        for (int j = i; j < n; j += k) {
            if (a[j] == 1) g--;
            else b--;
        }
        // cout << i << " " << g << " " << b << endl;
        ans = max(ans, abs(g - b));
    }

    cout << ans << endl;
}

