const int MOD = 1000000007;

ll modpow(ll x, ll y) { 
    ll res = 1;
    while (y) {
        if (y % 2) res = (res * x) % MOD;
        y /= 2;
        x = (x * x) % MOD;
    }
    return res; 
} 

