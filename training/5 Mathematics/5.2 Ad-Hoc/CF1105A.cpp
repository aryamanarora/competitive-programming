#include <bits/stdc++.h>

using namespace std;

typedef long long ll;
typedef long double ld;
typedef complex<ld> cd;

typedef pair<int, int> ii;
typedef tuple<int, int, int> iii;

typedef vector<int> vi;
typedef vector<ii> vii;
typedef vector<ld> vd;
typedef vector<ll> vl;

typedef set<int> si;
typedef set<ii> sii;
typedef set<ld> sd;
typedef set<ll> sl;

typedef map<int, int> mii;
typedef priority_queue<int> pqi;
typedef queue<int> qi;
 
#define mp make_pair
#define pb push_back
#define f first
#define s second

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n;
    cin >> n;
    vi a(n);
    for (auto &x : a) cin >> x;
    
    ii ans = {numeric_limits<int>::max(), -1};
    for (int i = 1; i <= 100; i++) {
        ll res = 0;
        for (auto &x : a) {
            res += min(abs(x - i), abs(abs(x - i) - 1));
        }
        ans = min(ans, {res, i});
    }

    cout << ans.s << " " << ans.f << endl;
}

