int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t;
    cin >> t;
    while (t--) {
        ll L, v, l, r;
        cin >> L >> v >> l >> r;
        if (l % v != 0) l = (l - l % v) + v;
        L -= L % v;
        r -= r % v;
        cout << (L / v) - (r - l) / v - 1 << endl;
    }
}

