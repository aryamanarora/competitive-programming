int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    ll n, k;
    cin >> n >> k;
    if (n > k * (k - 1)) {
        cout << "NO" << endl;
        return 0;
    }

    cout << "YES" << endl;
    for (int i = 1, ct = 0; i <= k and ct < n; i++) {
        for (int j = i + 1; j <= k and ct < n; j++) {
            cout << i << " " << j << endl;
            ct++;
            if (ct >= n) break;
            cout << j << " " << i << endl;
            ct++;
            if (ct >= n) break;
        }
    }
}

