int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n;
    cin >> n;
    int szx = 0, szy = 0;
    while (n--) {
        char a; int b, c;
        cin >> a >> b >> c;
        if (a == '+') {
            if (c > b) { szx = max(c, szx); szy = max(b, szy); }
            else { szx = max(b, szx); szy = max(c, szy); }
        }
        else {
            if (c > b) swap(b, c);
            if (b >= szx and c >= szy) cout << "YES" << endl;
            else cout << "NO" << endl;
        }
    }
}

