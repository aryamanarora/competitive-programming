int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    
    int t;
    cin >> t;
    while (t--) {
        ll y, x;
        cin >> y >> x;
        ll layer = max(y, x);
        ll pos;
        if (y >= x) pos = x;
        else pos = 2 * layer - y;
        if (layer % 2) cout << ((layer - 1) * (layer - 1)) + pos << endl;
        else cout << (layer * layer) - pos + 1 << endl;
    }
}

