#define mp make_pair
#define pb push_back
#define f first
#define s second

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n;
    cin >> n;
    map<string, int> pos;
    for (int i = 0; i < n; i++) {
        string s; cin >> s;
        pos[s] = i;
    }
    vector<pair<int, string>> a;
    for (auto &x : pos) a.pb({x.s, x.f});
    sort(a.rbegin(), a.rend());
    for (auto &x : a) cout << x.s << endl;
}

