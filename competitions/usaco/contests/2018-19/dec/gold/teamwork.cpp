int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    ifstream cin("teamwork.in");
    ofstream cout("teamwork.out");

    int n, k;
    cin >> n >> k;
    vi a(n); for (auto &x : a) cin >> x;
    vi dp(n);
    for (int i = 0; i < n; i++) {
        int maxi = 0;
        for (int j = i; j > i - k and j >= 0; j--) {
            maxi = max(maxi, a[j]);
            if (j > 0) dp[i] = max(maxi * (i - j + 1) + dp[j - 1], dp[i]);
            else dp[i] = max(maxi * (i - j + 1), dp[i]);
        }
    }
    cout << dp[n - 1] << endl;
}

