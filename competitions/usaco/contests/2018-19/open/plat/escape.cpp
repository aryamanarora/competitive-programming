const ll MOD = 1e9 + 7;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    ifstream cin("escape.in");
    ofstream cout("escape.out");

    int n, k;
    cin >> n >> k;
    mii ct;
    for (int i = 0, j; i < (n - 1) * k + n * (k - 1); i++) {
        cin >> j;
        ct[j]++;
    }

    ll ans = 1;
    for (auto &x : ct) {
        ans = ((ans % MOD) * (x.s % MOD)) % MOD;
    }
    cout << (ans == 10 ? 10 : 2) << endl;
}

