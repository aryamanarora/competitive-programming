int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    ifstream cin("highcard.in");
    ofstream cout("highcard.out");

    int n;
    cin >> n;
    si bessie;
    for (int i = 1; i <= 2 * n; i++) bessie.insert(i);
    vi a(n);
    for (auto &x : a) { cin >> x; bessie.erase(x); }

    int ans = 0;
    for (auto &x : a) {
        auto res = bessie.upper_bound(x);
        if (res != bessie.end()) {
            ans++;
            bessie.erase(res);
        }
    }

    cout << ans << endl;
}

